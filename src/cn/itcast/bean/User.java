package cn.itcast.bean;

public class User
{
  public String name;
  public String pass;

  public String getName()
  {
    return this.name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public String getPass() {
    return this.pass;
  }
  public void setPass(String pass) {
    this.pass = pass;
  }
}