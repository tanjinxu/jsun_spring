package cn.itcast.bean;

public class Stu
{
  public String name;
  public String age;
  public String sex;
  public int id;

  public int getId()
  {
    return this.id;
  }
  public void setId(int id) {
    this.id = id;
  }
  public String getName() {
    return this.name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public String getAge() {
    return this.age;
  }
  public void setAge(String age) {
    this.age = age;
  }
  public String getSex() {
    return this.sex;
  }
  public void setSex(String sex) {
    this.sex = sex;
  }
}